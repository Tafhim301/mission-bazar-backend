// ── Helpers ───────────────────────────────────────────────────────────────────
async function insertProduct(data, vendorId) {
  if (data.sku) {
    const exists = await Product.findOne({ sku: data.sku });
    if (exists) { process.stdout.write("·"); return "skipped"; }
  }

  const p = new Product({
    ...data,
    category: new mongoose.Types.ObjectId(data.category),
    vendor: vendorId,
    images: [],
    imagePublicIds: [],
    variants: data.variants || [],
    tags: data.tags || [],
    specifications: data.specifications || [],
    sold: 0,
    avgRating: 0,
    totalReviews: 0,
    freeShipping: false,
    status: "ACTIVE",
    isDeleted: false,
  });

  await p.save();
  process.stdout.write("+");
  return "inserted";
}

